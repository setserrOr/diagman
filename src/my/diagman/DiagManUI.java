    private void refreshListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshListActionPerformed
        
        // Enable the new button
        this.newButton.setEnabled(true);
        
        // Create a new list object by giving the connection object as input
        ModelL databaseList = new ModelL(con);
        // Create a patientInfo object by giving the connection object as input
        PatientInfo patientInfo = new PatientInfo(con);
        // Create a Contact object
        Contact contact = new Contact(con);
        
        // Let the jList element to know which model to use
        listInterface.setModel(databaseList.createListModel());
        
        // Save the List and PatientInfo instance in variable modelList
        modelList = databaseList;
        pInfo = patientInfo;
        cInfo = contact;
        
        
    }//GEN-LAST:event_refreshListActionPerformed

