    private void doConnectionActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_doConnectionActionPerformed
    {//GEN-HEADEREND:event_doConnectionActionPerformed
        // Read server address, username and password from the dialog's fields
        this.host = this.serverAddress.getText();
        this.uName = this.usernameInput.getText();
        char [] pass = this.passwordInput.getPassword();
        this.uPass = new String(pass);
        
        // Perform connection (or at least try to do so!) 
        try 
        {
            // Load the Derby driver
            Class.forName("org.apache.derby.jdbc.ClientDriver");
        } 
        catch (ClassNotFoundException cnfe) 
        {
            System.err.println("Derby driver not found.");
        }
        
        try
        {
            con = DriverManager.getConnection(host, uName, uPass);
            this.statusBar.setText("connetion successful!");
            this.connectionDialog.setVisible(false);
        }
        catch (SQLException err)
        {
            System.out.println(err.getMessage());
        }    
        
    }//GEN-LAST:event_doConnectionActionPerformed

