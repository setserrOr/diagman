    private void listInterfaceValueChanged(javax.swing.event.ListSelectionEvent evt)//GEN-FIRST:event_listInterfaceValueChanged
    {//GEN-HEADEREND:event_listInterfaceValueChanged
        if (this.editButton.isEnabled() == false)
            this.editButton.setEnabled(true);
        if (this.deleteButton.isEnabled() == false)
            this.deleteButton.setEnabled(true);

        // Create patientInfo, contactInfo, diagnosisResults and caseInfo objects for the corresponding selection
        // Create a string from the selection in the list box
        int selected = listInterface.getLeadSelectionIndex();
        System.out.println("selected:" + selected);
        
        // Get the patient ID from the selected item in the list
        int id = modelList.getId(selected);
        pInfo.setIdNum(id);
        cInfo.setIdNum(pInfo.getContact());
        
        // Populate the Patient info tab
        this.fNameField.setText(pInfo.getFName());
        this.lNameField.setText(pInfo.getLName());
        this.bDateField.setText(pInfo.getBDate());
        this.genderField.setSelectedIndex(pInfo.getGender());
        this.nationalityField.setText(pInfo.getNationality());
        
        //System.out.println("new" + pInfo.getGender());
        this.occupationField.setText(pInfo.getOccupation());
        this.smokerBox.setSelected(pInfo.getSmoker());
        this.allergiesBox.setSelected(pInfo.getAllergies());
        
        // Populate the Contact tab
        this.addressField.setText(cInfo.address);
        this.phoneField.setText(cInfo.phone);
        this.mobileField.setText(cInfo.mobile);
        this.emailField.setText(cInfo.email);
        this.postalField.setText(cInfo.postalCode);
        this.cityField.setText(cInfo.city);
        this.countryField.setText(cInfo.country);
    }//GEN-LAST:event_listInterfaceValueChanged

